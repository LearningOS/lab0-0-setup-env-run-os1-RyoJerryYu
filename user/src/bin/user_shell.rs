#![no_std]
#![no_main]

extern crate alloc;

#[macro_use]
extern crate user_lib;

const LF: u8 = 0x0au8;
const CR: u8 = 0x0du8;
const DL: u8 = 0x7fu8;
const BS: u8 = 0x08u8;

use alloc::string::String;
use user_lib::console::getchar;
use user_lib::{exec, fork, waitpid};

#[no_mangle]
pub fn main() -> i32 {
    println!("Rust user shell");
    let mut line: String = String::new();
    print!(">> ");
    loop {
        let c = getchar();
        // for each character:
        match c {
            // if it is a newline or carriage return:
            // 1. print a newline
            // 2. execute the command in forked process
            // 3. in parent process, wait for the child process to exit
            LF | CR => {
                println!("");
                if !line.is_empty() {
                    line.push('\0');
                    let pid = fork();
                    if pid == 0 {
                        // child process
                        if exec(line.as_str()) == -1 {
                            println!("Error when executing!");
                            return -4;
                        }
                        unreachable!();
                    } else {
                        let mut exit_code: i32 = 0;
                        let exit_pid = waitpid(pid, &mut exit_code);
                        assert_eq!(pid, exit_pid);
                        println!("Shell: Process {} exited with code {}", pid, exit_code);
                    }
                    line.clear();
                }
                // finally, print prompt
                print!(">> ");
            }
            // if it is a backspace or delete:
            BS | DL => {
                if !line.is_empty() {
                    // backward the cursor
                    print!("{}", BS as char);
                    // print a space, overwrite the character
                    print!(" ");
                    // backward the cursor again, now the cursor is at the position of the character to be deleted
                    print!("{}", BS as char);
                    // remove the last character
                    line.pop();
                }
            }
            _ => {
                print!("{}", c as char);
                line.push(c as char);
            }
        }
    }
}
